package com.Firstcode;

public class Switch {

	public static void main(String[] args) {
		int x=34;
		switch(x) {
		case 1:System.out.println("one");
		break;
		case 2:System.out.println("two");
		break;
		case 3:System.out.println("three");
		break;
		default:System.out.println("this is not a number");
		}
}

}
